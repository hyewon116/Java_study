package ch03;

//관계 연산자(비교 연산자) -> 결과가 true, false.
//>, <, >=, <=, ==(같다), !=(다르다)
public class Ch03Ex02 {
	
	public static void main(String[] args) {
		
		System.out.println(10 > 100);//false
		System.out.println(10 < 100);//true
		System.out.println(10 >= 100);//false
		System.out.println(10 <= 100);//true
		System.out.println(10 == 100);//false
		System.out.println(10 != 100);//true
		
	}//main

}//class
